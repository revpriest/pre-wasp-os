class MetalFontApp():
    """ Metal font """

    NAME = "Metal"
    ICON = METALFONT_DIGITS[5]

    def __init__(self):
        if(not hasattr(wasp,"fonts")): 
            wasp.fonts = {}
        wasp.fonts['Metal'] = (5,METALFONT_DIGITS)   #ID:5. Please use random > 100 for user-fonts
        pass

    def unregister(self):
        del(wasp.fonts['Metal'])

    def foreground(self):
        self._draw()


    def _draw(self):
        draw = wasp.watch.drawable
        draw.fill()
        draw.blit(METALFONT_DIGITS[0],    0,  60)
        draw.blit(METALFONT_DIGITS[1],   45,  60)
        draw.blit(METALFONT_DIGITS[2],   90,  60)
        draw.blit(METALFONT_DIGITS[3],  135,  60)
        draw.blit(METALFONT_DIGITS[4],  180,  60)
        draw.blit(METALFONT_DIGITS[5],    0, 125)
        draw.blit(METALFONT_DIGITS[6],   45, 125)
        draw.blit(METALFONT_DIGITS[7],   90, 125)
        draw.blit(METALFONT_DIGITS[8],  135, 125)
        draw.blit(METALFONT_DIGITS[9],  180, 125)


