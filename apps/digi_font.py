class DigiFontApp():
    """Digifont"""
    NAME = "Digi"
    ICON = DIGIFONT_DIGITS[4]

    def __init__(self):
        if(not hasattr(wasp,"fonts")): 
            wasp.fonts = {}
        wasp.fonts['Digi'] = (4,DIGIFONT_DIGITS)   #ID:4. Please use random > 100 for user-fonts
        pass

    def unregister(self):
        del(wasp.fonts['Digi'])

    def foreground(self):
        self._draw()


    def _draw(self):
        draw = wasp.watch.drawable
        draw.fill()
        draw.blit(DIGIFONT_DIGITS[0],    0,  60)
        draw.blit(DIGIFONT_DIGITS[1],   45,  60)
        draw.blit(DIGIFONT_DIGITS[2],   90,  60)
        draw.blit(DIGIFONT_DIGITS[3],  135,  60)
        draw.blit(DIGIFONT_DIGITS[4],  180,  60)
        draw.blit(DIGIFONT_DIGITS[5],    0, 125)
        draw.blit(DIGIFONT_DIGITS[6],   45, 125)
        draw.blit(DIGIFONT_DIGITS[7],   90, 125)
        draw.blit(DIGIFONT_DIGITS[8],  135, 125)
        draw.blit(DIGIFONT_DIGITS[9],  180, 125)


